import React from 'react';
import ReactDOM from 'react-dom/client';
import { ClerkProvider } from '@clerk/clerk-react';
import App from './App.jsx';
import './index.css';

// Import your publishable key
const PUBLISHABLE_KEY = import.meta.env.VITE_CLERK_PUBLISHABLE_KEY
const savedTheme = typeof window !== 'undefined' ? window.localStorage.getItem('timetable-theme') : null;
if (savedTheme === 'light' || savedTheme === 'dark') {
  document.documentElement.setAttribute('data-theme', savedTheme);
}

if (!PUBLISHABLE_KEY) {
  const root = document.getElementById('root');
  if (root) {
    root.innerHTML = '<div style="color: red; padding: 20px;"><h1>Error: VITE_CLERK_PUBLISHABLE_KEY is missing</h1><p>Check your .env.local file.</p></div>';
  }
  throw new Error("Missing Publishable Key");
}

ReactDOM.createRoot(document.getElementById('root')).render(
  <React.StrictMode>
    <ClerkProvider publishableKey={PUBLISHABLE_KEY} afterSignOutUrl="/">
      <App />
    </ClerkProvider>
  </React.StrictMode>,
);
